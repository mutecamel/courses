# 遍历字典中的每个歌手
for singer, details in my_dict.items():
    # 检查级别是否为1
    if details["级别"] == 1:
        # 更新级别和工资
        details["级别"] = 2
        details["工资"] += 1000

    # 打印更新后的字典以验证更改
for singer, details in my_dict.items():
    print(f"歌手: {singer}")
    print(f"  部门: {details['部门']}")
    print(f"  工资: {details['工资']}")
    print(f"  级别: {details['级别']}")
